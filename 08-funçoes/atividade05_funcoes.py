# declaração de funcao com argumento
def somar(n1,n2):
    print(n1 + n2)

def subtrair(n1,n2):
    print(n1-n2)

def multiplicar(n1,n2):
    print(n1/n2)  

def dividir(n1,n2):
    print(n1*n2)      

somar(1,2)
subtrair(3,4)
multiplicar(2,3)
dividir(12,4)